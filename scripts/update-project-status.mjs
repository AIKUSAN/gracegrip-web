#!/usr/bin/env node
/**
 * scripts/update-project-status.mjs
 *
 * Regenerates .claude/rules/PROJECT_STATUS.md before every push.
 * Invoked automatically by .githooks/pre-push.
 * Also runnable manually: node scripts/update-project-status.mjs
 *
 * No external deps — pure Node.js ESM only.
 */

import { spawnSync } from 'child_process'
import { readFileSync, writeFileSync, readdirSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')

// Use spawnSync with an args array — no shell interpolation, no injection surface.
function git(args) {
  const result = spawnSync('git', args, { cwd: ROOT, encoding: 'utf8' })
  if (result.error || result.status !== 0) return '(unavailable)'
  return result.stdout.trim()
}

// ── Gather facts ──────────────────────────────────────────────────────────────

const pkg = JSON.parse(readFileSync(join(ROOT, 'package.json'), 'utf8'))
const version = pkg.version ?? '(unknown)'

const branch = git(['branch', '--show-current']) || git(['rev-parse', '--abbrev-ref', 'HEAD'])
const lastCommitHash = git(['rev-parse', '--short', 'HEAD'])
const lastCommitMsg = git(['log', '-1', '--pretty=%s'])
const lastCommitDate = git(['log', '-1', '--pretty=%ci']).split(' ')[0] // YYYY-MM-DD

const recentCommits = git(['log', '--oneline', '-15'])

const rulesFiles = readdirSync(join(ROOT, '.claude', 'rules'))
  .filter((f) => f.endsWith('.md'))
  .sort()

const now = new Date().toISOString().replace('T', ' ').slice(0, 19) + ' UTC'

// ── Build the status document ─────────────────────────────────────────────────

const md = `# GraceGrip — Project Status
<!-- AUTO-GENERATED by scripts/update-project-status.mjs on every git push. Do not edit by hand. -->

## Version

| Field | Value |
|-------|-------|
| Version | \`${version}\` |
| Branch | \`${branch}\` |
| Last commit | \`${lastCommitHash}\` — ${lastCommitMsg} |
| Commit date | ${lastCommitDate} |
| Status generated | ${now} |

---

## Architecture Snapshot

**Stack:** Next.js 16 App Router + React 19 + Tailwind CSS v4
**Deploy:** Vercel hybrid mode — static pages + Node.js serverless functions
**Live URL:** https://gracegrip.app
**State:** Single React Context (\`src/context/AppContext.jsx\`), persisted to localStorage key \`gracegrip_v1\`

### Routes

| Route | File |
|-------|------|
| \`/\` | \`app/page.jsx\` — Home: streak ring, greeting, daily verse |
| \`/emergency\` | \`app/emergency/page.jsx\` — Urge timer, breathing, grounding, panic verse |
| \`/scripture\` | \`app/scripture/page.jsx\` — Category-based verse browser |
| \`/devotional\` | \`app/devotional/page.jsx\` — Daily devotional reader |
| \`/journal\` | \`app/journal/page.jsx\` — Encrypted private journal |
| \`/settings\` | \`app/settings/page.jsx\` — Theme, profile, backup, feedback |
| \`/api/og\` | \`app/api/og/route.js\` — Serverless OG image (Puppeteer screenshot + fallback card) |

### Key Dependencies

| Package | Version |
|---------|---------|
| next | \`${pkg.dependencies?.next ?? '?'}\` |
| react | \`${pkg.dependencies?.react ?? '?'}\` |
| tailwindcss | \`${pkg.dependencies?.tailwindcss ?? '?'}\` |
| motion | \`${pkg.dependencies?.motion ?? '?'}\` |
| @supabase/supabase-js | \`${pkg.dependencies?.['@supabase/supabase-js'] ?? '?'}\` |
| puppeteer-core | \`${pkg.dependencies?.['puppeteer-core'] ?? '?'}\` |
| @sparticuz/chromium | \`${pkg.dependencies?.['@sparticuz/chromium'] ?? '?'}\` |

---

## Active Features (v${version})

- **Streak tracker** — daily check-in, unbounded count, reset on miss/stumble, milestone messages
- **Panic Button / Emergency flow** — urge timer → breathing exercise → grounding → Scripture verse
- **Scripture browser** — emotion-filtered verse library
- **Daily devotional** — deterministic rotation (\`dayOfYear % devotionals.length\`)
- **Encrypted journal** — AES via Web Crypto API, entries never leave device
- **Backup / restore** — JSON export + import, QR-based device transfer
- **Dark mode** — light / dark / system, persisted to localStorage
- **OG image** — Puppeteer screenshot at \`/api/og\`, branded fallback card via \`next/og\`
- **Anonymous feedback** — optional Supabase channel (only external network call)

---

## Recent Commits (last 15)

\`\`\`
${recentCommits}
\`\`\`

---

## Claude Code Rules Files

The following files in \`.claude/rules/\` are auto-loaded by Claude Code as project instructions:

${rulesFiles.map((f) => `- \`.claude/rules/${f}\``).join('\n')}

> **Read PROJECT_STATUS.md first** in any new session to understand current app state before making changes.
`

// ── Write output ──────────────────────────────────────────────────────────────

const outPath = join(ROOT, '.claude', 'rules', 'PROJECT_STATUS.md')
writeFileSync(outPath, md, 'utf8')

console.log(`[update-project-status] Written: .claude/rules/PROJECT_STATUS.md (v${version}, ${lastCommitHash})`)
